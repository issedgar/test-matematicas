module.exports = {
    /**
     * Suma de dos numeros
     * @example
     * n1 = 1, n2 = 3 => resultado 4
     * @param {*} n1 Numero uno de la suma
     * @param {*} n2 Numero dos de la suma
     * @returns 
     * number
     */
    suma: function(n1, n2) {
        return n1 + n2;
    },
    resta: function(n1, n2) {
        return n1 - n2;
    },
    multiplicacion: function(n1, n2) {
        return n1 * n2;
    },
    division: function(n1, n2) {
        return n1 / n2;
    }
}
